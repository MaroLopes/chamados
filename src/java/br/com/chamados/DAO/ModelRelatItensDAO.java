    public void delete(ModelRelatItens modelrelatitens){
        EntityManager em = getEntityManager();
        
        try {
            
            em.getTransaction().begin();
            ModelRelatItens m = em.find(ModelRelatItens.class, modelrelatitens.getModelRelatItensId());
            
            em.remove(m);
            em.getTransaction().commit();
            
        } catch (Exception e) {
            FacesContext.getCurrentInstance().addMessage(null, new FacesMessage(FacesMessage.SEVERITY_INFO, "ERRO",  e.getMessage()));
            em.getTransaction().rollback();
            em.close();
        }
    }
    
    public List<ModelRelatItens> GetALL(){
        EntityManager em = getEntityManager();
        List<ModelRelatItens> lista = null;
        
        try {
            
            Query q =  em.createQuery("select object (M) from ModelRelatItens as M");
            lista = q.getResultList();
            
        } catch (Exception e) {
            FacesContext.getCurrentInstance().addMessage(null, new FacesMessage(FacesMessage.SEVERITY_INFO, "ERRO",  e.getMessage()));
            em.close();
        }
        
        return lista;
    }
    
